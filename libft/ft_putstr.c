#include "libft.h"

int	ft_putstr(char *s)
{
	int	x;

	x = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (s[x] != '\0')
	{
		ft_putchar(s[x]);
		x++;
	}
	return (x);
}
